package classes;

public class AnimationEntity {  //PAS UTILISER
	
	private int nbFrames;
	private int frameBefore;
	private int frameCount;
	private int direction;
	private int nbDirections;
	
	
	public AnimationEntity(int nbFrames, int frameBefore, int direction, int nbDirections) {
		this.nbFrames = nbFrames;
		this.frameBefore = frameBefore;
		this.frameCount = 0;
		this.direction = direction;
		this.nbDirections = nbDirections;
	}
	
	
	
	
	
	
}
